import mongoose from "mongoose";

const WorkExperienceSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  logo: {
    type: String,
    required: true,
  },
  icon: {
    type: String,
    required: true,
  },
  startDate: {
    type: Date,
    required: true,
  },
  endDate: {
    type: Date,
    required: false,
  },
});

const WorkExperience = mongoose.model("WorkExperience", WorkExperienceSchema);

export default WorkExperience;
